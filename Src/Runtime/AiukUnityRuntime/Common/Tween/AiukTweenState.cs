namespace AiukUnityRuntime.Tween
{
    /// <summary>
    /// 动画运行状态
    /// </summary>
    public enum AiukTweenState
    {
        /// <summary>
        /// 动画启动
        /// </summary>
        Start,

        /// <summary>
        /// 动画运行中
        /// </summary>
        Running,

        /// <summary>
        /// 动画暂停
        /// </summary>
        Paused,

        /// <summary>
        /// 动画恢复
        /// </summary>
        Resume,

        /// <summary>
        /// 动画停止
        /// </summary>
        Stopped
    }
}
